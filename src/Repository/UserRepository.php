    /**
     * @return boolean return if object found
     */
    public function findByNameAndNotId($name, $id)
    {
        $result = $this
            ->createQueryBuilder('g')
            ->where('g.username = :name')
            ->setParameter('name', $name)
            ->andWhere('g.id != :id')
            ->setParameter('id', $id)
            ->getQuery()
            ->getArrayResult();

        if ($result){
            return true;
        }
        return false;
    }

